void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];
    for (int i = 0; i < n - 1; i++)
        if (a[i] > a[i + 1])
        {
            cout << "NO\n";
            return;
        }
        else
        {
            int _min = min(a[i], a[i + 1]);
            a[i] -= _min;
            a[i + 1] -= _min;
        }
    cout << "YES\n";
}

