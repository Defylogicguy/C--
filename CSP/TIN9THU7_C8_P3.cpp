void solve()
{
    int n, d;
    cin >> n >> d;
    vector<pair<int, int>> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i].first >> a[i].second;
    sort(all(a), [](const pair<int, int> &a, const pair<int, int> &b)
         { return a.second < b.second; });
    int ans = 0, lst = -1;
    for (int i = 0; i < n; i++)
        if (a[i].first > lst)
        {
            ans++;
            lst = a[i].second + d - 1;
        }
    cout << ans;
}

