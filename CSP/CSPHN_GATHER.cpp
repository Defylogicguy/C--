#define NAME "CSPHN_GATHER"

void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];
    sort(all(a));
    int mid = a[n / 2];
    int ans = 0;
    for (int i : a)
        ans += abs(i - mid);
    cout << ans;
}

