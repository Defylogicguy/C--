void solve()
{
    int x, n;
    cin >> x >> n;
    heap<int, vector<int>, greater<int>> pq;
    for (int i = 0, x; i < n; i++)
    {
        cin >> x;
        pq.push(x);
    }
    int ans = 0;
    while (pq.size() > 1)
    {
        int a = pq.top();
        pq.pop();
        int b = pq.top();
        pq.pop();
        ans += a + b;
        pq.push(a + b);
    }
    cout << ans;
}

