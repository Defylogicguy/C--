void solve()
{
    int n;
    cin >> n;
    int off = 1000;
    int ans = 0;
    for (int a = max(0ll, n / 3 - off); a <= n / 3 + off; a++)
        for (int b = max(0ll, (n - a) / 2 - off); b <= (n - a) / 2 + off; b++)
        {
            if (n - a - b < 0)
                continue;
            ans = max(ans, (a & b & (n - a - b)));
        }
    cout << ans;
}

