void solve()
{
    int n;
    cin >> n;
    vector<int> a(n + 1);
    for (int i = 1; i <= n; i++)
        cin >> a[i];
    vector<int> pf(n + 1, LLONG_MAX);
    pf[1] = a[1];
    for (int i = 2; i <= n; i++)
        pf[i] = min(pf[i - 1], a[i]);
    vector<int> sf(n + 2, LLONG_MIN);
    sf[n] = a[n];
    for (int i = n - 1; i >= 1; i--)
        sf[i] = max(sf[i + 1], a[i]);
    int ans = 0;
    for (int i = 1; i < n; i++)
        ans += (pf[i] == sf[i + 1]);
    cout << ans;
}

