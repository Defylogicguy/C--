void solve()
{
    int l, g;
    cin >> g >> l;
    if (l % g != 0)
    {
        cout << -1 << '\n';
        return;
    }
    int k = l / g;
    int ans = LLONG_MAX;
    for (int i = 1; i * i <= k; i++)
        if (k % i == 0 and __gcd(i, k / i) == 1)
            ans = min(ans, i + k / i);
    cout << (ans == LLONG_MAX ? -1 : ans * g) << '\n';
}

