#define MOD 1000000007
#define NAME "LMH_TREEWALK"

void solve()
{
    string s;
    cin >> s;
    int ans = 1, t = 1;
    for (char c : s)
        if (c == 'L')
            (ans *= 2) %= MOD;
        else if (c == 'R')
            (ans = ans * 2 + t) %= MOD;
        else if (c == '*')
            (ans = ans * 5 + t) %= MOD, (t *= 3) %= MOD;
    cout << ans;
}

