void solve()
{
    int n, k;
    cin >> n >> k;
    vector<int> a(n + 1);
    for (int i = 1; i <= n; i++)
        cin >> a[i];
    sort(1 + all(a), greater<int>());
    vector<vector<int>> dp(n + 1, vector<int>(n / 3 + 1, LLONG_MAX));
    dp[0][0] = 0;
    for (int i = 1; i <= n; i++)
        for (int j = 0; j <= min(i / 3, k); j++)
        {
            dp[i][j] = dp[i - 1][j];
            if (i >= 2 and j)
                dp[i][j] = min(dp[i][j], dp[i - 2][j - 1] + (a[i] - a[i - 1]) * (a[i] - a[i - 1]));
        }
    cout << dp[n][k];
}

