inline void solve()
{
    int n;
    cin >> n;
    string s;
    cin >> s;
    int mask = 0, ans = 0;
    unordered_map<int, int> mp;
    mp[mask]++;
    for (int i = 0; i < n; i++)
    {
        mask ^= (1 << (s[i] - 'a'));
        auto it = mp.find(mask);
        if (it != mp.end())
            ans += it->second;
        for (int d = 0; d < 26; d++)
        {
            int nw = mask ^ (1 << d);
            it = mp.find(nw);
            if (it != mp.end())
                ans += it->second;
        }
        mp[mask]++;
    }
    cout << ans;
}

