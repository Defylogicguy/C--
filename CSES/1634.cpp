void solve()
{
    int n, m;
    cin >> n >> m;

    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];

    vector<int> dp(m + 1, INT_MAX);
    dp[0] = 0;
    for (int i = 1; i <= m; i++)
        for (int j = 0; j < n; j++)
            if (i - a[j] >= 0 && dp[i - a[j]] != INT_MAX)
                dp[i] = min(dp[i], dp[i - a[j]] + 1);
    cout << (dp[m] == INT_MAX ? -1 : dp[m]);
}

