void solve()
{
    string s;
    cin >> s;
    int ans = 1, cnt = 1;
    for (int i = 0; i < s.size() - 1; i++)
        if (s[i] == s[i + 1])
            cnt++;
        else
        {
            ans = max(ans, cnt);
            cnt = 1;
        }
    ans = max(ans, cnt);
    cout << ans;
}

