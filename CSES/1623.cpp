void dfs(int idx, int cur, const vector<int> &a)
{
    if (idx == a.size())
    {
        ans = min(ans, abs(sum - 2 * cur));
        return;
    }
    dfs(idx + 1, cur, a);
    cur += a[idx];
    dfs(idx + 1, cur, a);
}

