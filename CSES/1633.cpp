const int N = 1e6 + 1;
vector<int> dp(N);
int idk(int n)
{
    if (dp[n] != 0)
        return dp[n];
    for (int i = 1; i <= 6; i++)
        if (n - i >= 0)
        {
            dp[n] += idk(n - i);
            dp[n] %= MOD;
        }
    return dp[n];
}

