void solve()
{
    int n, m;
    cin >> n >> m;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];
    sort(all(a));
    int ans = 0, j = 0;
    for (int i = 0; i < n; i++)
    {
        while (j < n && a[j] - a[i] < m)
            j++;
        ans = max(ans, j - i);
    }
    cout << ans << endl;
}

