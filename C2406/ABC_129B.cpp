void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];
    vector<int> pf(n + 1, 0);
    partial_sum(all(a), pf.begin() + 1);
    int ans = INT_MAX;
    for (int i = 1; i <= n; i++)
        ans = min(ans, abs(pf[n] - pf[i] - pf[i] + pf[0]));
    cout << ans << endl;
}

