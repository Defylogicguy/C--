void solve()
{
    int n, m;
    cin >> n >> m;
    map<int, int> mp;
    for (int i = 0; i < n; i++)
    {
        int k;
        cin >> k;
        for (int j = 0; j < k; j++)
        {
            int x;
            cin >> x;
            mp[x]++;
        }
    }

    int ans = 0;
    for (int i = 1; i <= m; i++)
        ans += (mp[i] == n);
    cout << ans;
}

