void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];
    int ans = LLONG_MAX;
    for (int i = 1; i <= 100; i++)
    {
        int cnt = 0;
        for (int j : a)
            cnt += (j - i) * (j - i);
        ans = min(ans, cnt);
    }
    cout << ans;
}

