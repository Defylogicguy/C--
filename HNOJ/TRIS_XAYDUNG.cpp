void solve()
{
    int a, b, c, d;
    cin >> a >> b >> c >> d;
    int ans = 0;
    for (int i = 1; i * i <= b; i++)
    {
        int l = max({i, (a + i - 1) / i, (c + 1) / 2 - i});
        int r = min(b / i, d / 2 - i);
        if (l <= r)
            ans += r - l + 1;
    }
    cout << ans;
}

