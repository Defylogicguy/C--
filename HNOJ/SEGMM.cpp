void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];

    int mx = maxall(a), mn = minall(a), ans = n, mxx = -1, mnn = -1;

    for (int i = 0; i < n; i++)
    {
        if (a[i] == mx)
        {
            mxx = i;
            if (mnn != -1)
                ans = min(ans, mxx - mnn + 1);
        }
        if (a[i] == mn)
        {
            mnn = i;
            if (mxx != -1)
                ans = min(ans, mnn - mxx + 1);
        }
    }

    cout << ans;
}

