void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];
    sort(all(a));
    int maxa = a.back();
    vector<int> dp(maxa + 1);
    dp[0] = 1;
    int ans = 0;
    for (int i = 0; i < n; i++)
    {
        int idk = 0;
        for (int j = 0; j <= a[i]; j++)
            idk += dp[j];
        int t = (1ll << i) - idk;
        ans += t;
        for (int j = maxa; j >= a[i]; j--)
            dp[j] += dp[j - a[i]];
    }
    cout << ans;
}

