void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];

    int i = 0, j = n - 1;
    int cnt = 0;

    while (i < j)
    {
        if (a[i] == a[j])
        {
            i++;
            j--;
        }
        else if (a[i] < a[j])
        {
            a[i + 1] += a[i];
            i++;
            cnt++;
        }
        else
        {
            a[j - 1] += a[j];
            j--;
            cnt++;
        }
    }
    cout << cnt;
}

