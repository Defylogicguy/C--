void solve()
{
    int a, b;
    cin >> a >> b;
    auto f = [](int a, int b, int p) -> int
    {
        int cnt = 0, cur = p;
        while (cur <= b)
        {
            int l = (a + cur - 1) / cur, r = b / cur;
            if (l <= r)
                cnt++;
            else
                break;
            cur *= p;
        }
        return cnt;
    };
    cout << min(f(a, b, 2), f(a, b, 5));
}

