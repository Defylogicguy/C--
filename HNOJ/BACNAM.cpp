void solve()
{
    int n;
    cin >> n;
    vector<int> pos(n + 1);
    for (int i = 1; i <= n; i++)
    {
        int x;
        cin >> x;
        pos[x] = i;
    }
    auto f = [n, pos](int st) -> int
    {
        int cnt = 0, cur = pos[st];
        for (int i = st; i <= n; i += 2)
        {
            if (pos[i] <= cur)
                cnt++;
            cur = pos[i];
        }
        return cnt;
    };
    cout << f(1) << '\n'
         << f(2);
}

